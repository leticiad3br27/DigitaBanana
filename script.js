    function startGame() {
      gameState.mode = elements.modeSelect.value;
      gameState.difficulty = elements.difficultySelect.value;
      gameState.timeLeft = 60;
      gameState.wpm = 0;
      gameState.accuracy = 100;
      gameState.score = 0;
      gameState.combo = 0;
      gameState.maxCombo = 0;
      gameState.currentIndex = 0;
      gameState.totalChars = 0;
      gameState.correctChars = 0;
      gameState.wrongChars = 0;
      gameState.completedWords = 0;
      gameState.gameStarted = true;
      gameState.lastError = '';

      elements.menu.classList.add('hidden');
      elements.game.classList.remove('hidden');
      elements.results.classList.add('hidden');
      elements.capsWarning.classList.remove('active');
      elements.errorMessage.classList.remove('active');

      updateDisplay();
      nextText();
      elements.inputField.focus();
      startTimer();
    }

    function startTimer() {
      gameState.timer = setInterval(() => {
        gameState.timeLeft--;
        elements.time.textContent = gameState.timeLeft;

        if (gameState.timeLeft <= 0) {
          endGame();
        }
      }, 1000);
    }

    function nextText() {
      gameState.currentText = getRandomText();
      gameState.currentIndex = 0;
      
      const lettersHTML = gameState.currentText.split('').map((char, idx) => 
        `<span class="letter${idx === 0 ? ' current' : ''}">${char}</span>`
      ).join('');
      
      elements.displayText.innerHTML = lettersHTML;
      elements.inputField.value = '';
      
      elements.animationElement.className = 'bouncing-ball';
      
      setTimeout(() => updateAnimationPosition(), 100);
    }

    function handleInput(e) {
      if (!gameState.gameStarted) return;

      checkCapsLock(e);

      const input = elements.inputField.value;
      const letters = document.querySelectorAll('.letter');
      
      letters.forEach((letter, idx) => {
        letter.className = 'letter';
        if (idx < input.length) {
          if (input[idx] === gameState.currentText[idx]) {
            letter.classList.add('typed');
            if (idx === input.length - 1) {
              createParticles(letter);
            }
          } else {
            letter.classList.add('wrong');
          }
        } else if (idx === input.length) {
          letter.classList.add('current');
        }
      });

      gameState.currentIndex = input.length;
      updateAnimationPosition();

      if (input.length === gameState.currentText.length) {
        if (input === gameState.currentText) {
          gameState.correctChars += gameState.currentText.length;
          gameState.combo++;
          gameState.maxCombo = Math.max(gameState.maxCombo, gameState.combo);
          gameState.completedWords++;
          
          const multiplier = 1 + (gameState.combo * 0.1);
          const basePoints = gameState.currentText.length * (gameState.difficulty === 'easy' ? 10 : gameState.difficulty === 'medium' ? 15 : 25);
          gameState.score += Math.floor(basePoints * multiplier);
          
          showComboMessage(gameState.combo);
          
          setTimeout(() => nextText(), 200);
        } else {
          gameState.wrongChars += gameState.currentText.length;
          gameState.combo = 0;
          
          const errorMsg = detectError(input, gameState.currentText);
          showError(errorMsg);
          
          elements.inputField.value = '';
          gameState.currentIndex = 0;
          
          letters.forEach((letter, idx) => {
            letter.className = 'letter';
            if (idx === 0) letter.classList.add('current');
          });
          updateAnimationPosition();
        }
        
        gameState.totalChars += gameState.currentText.length;
        updateStats();
      }
    }

    function updateStats() {
      const elapsedTime = (60 - gameState.timeLeft) / 60;
      gameState.wpm = elapsedTime > 0 ? Math.floor(gameState.completedWords / elapsedTime) : 0;
      gameState.accuracy = gameState.totalChars > 0 ? Math.floor((gameState.correctChars / gameState.totalChars) * 100) : 100;

      updateDisplay();
    }

